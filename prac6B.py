# Create a tuple of favorite colors
favorite_colors = ("Blue", "Green", "Red", "Yellow")
print("Original Tuple:", favorite_colors)

# a. Access the first and last elements of the tuple
first_element = favorite_colors[0]
last_element = favorite_colors[-1]
print("First element:", first_element)
print("Last element:", last_element)

# b. Concatenate two tuples together
additional_colors = ("Purple", "Orange")
concatenated_tuple = favorite_colors + additional_colors
print("After concatenation:", concatenated_tuple)

# c. Repeat a tuple three times and display the result
repeated_tuple = favorite_colors * 3
print("Repeated Tuple:", repeated_tuple)
